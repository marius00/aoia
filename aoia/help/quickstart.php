<h1>AO Item Assistant : The Quick Start Guide</h1>

<p class="bread">
A short summary of how to get started using AO Item Assistant.<br />
</p>
<ul>
<li>Launch AO Item Assistant and Anarchy Online.</li>
<li>After the mandatory database ripping (which may take some time) 
AO Item Assistant is now listening to you Anarchy Online client if it 
is running.</li>
<li>Zone once, and open all backpacks in your inventory.</li>
<li>Backpacks in the bank don't need to be opened, just moved to your
inventory and back into the bank.<br />(Tip: Rightclick backpacks in 
the bank to transfer them to the first available slot in your inventory.)</li>
<li>To search for an item, go to the "Inventory" page and press CTRL-F. A 
small input field will become available in the top of the view, where you can 
type in parts of the name of the item you are looking for.
<li>Other tools may be selected by choosing a different page close to the top 
of the main window. "Patter Matcher" and "Player Shop Monitor" are examples of 
two such tools.</li>
</ul>

