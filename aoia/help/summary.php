<h1>AO Item Assistant : The Summary</h1>

<h2>Introduction</h2>
<p>The Summary View gives a quick overview of the recorded characters. It 
shows their name, level and amount of credits. Each dimension is displayed and 
summarized by itself.</p>

<center><a href="summary.jpg" target="_blank"><img src="summary-50.jpg" style="margin:5px;" /></a></center>

<p>If you are missing characters from the summary page, it is probably because 
their stats haven't been recorded yet. To fix this, simply log in using the 
character you wish to display and hit the "Refresh" button on the toolbar.</p>

