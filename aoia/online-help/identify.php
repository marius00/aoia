<h1>Item Assistant Help : The Identify View</h1>

<h2>Introduction</h2>
<p>This feature was introduced with version 0.9.2. It will let you swiftly 
locate unconverted bio-clumps from aliens that turn into specific types. 
It also has a few other select items (like galactic gems) that you can search 
for.</p>

<center><img src="identify.jpg" width="512" style="margin:5px;" /></center>


<h2>The GUI Explained</h2>
<p>The GUI is divided into two different lists. The left-hand side list is 
where you select what items you want to search for, and the right-hand side
list is where the results of the search appear.</p>


